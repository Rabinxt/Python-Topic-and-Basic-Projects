# Circle
def area_circle(r):
    pi = 3.14
    area = r**2*pi
    return area

def perimeter_circle(r):
    pi = 3.14
    perimeter = 2*r*pi
    return perimeter

# Square

def area_square(side):
    return side * side
def perimeter_square(side):
    return side * 4

# Rectangle

def area_rectangle(a,b):
    return a*b

def perimeter_rectangle(a,b):
    return 2*(a+b)